import { createBrowserRouter } from "react-router-dom";
import App from "./App";
import { Home, Login, Profile } from "./screens";

const router = createBrowserRouter([
  {
    path: "/",
    element: <App />,
    children: [
      {
        path: "/home",
        element: <Home />,
      },
      {
        path: "/profile",
        element: <Profile />,
      },
    ],
  },
  {
    path: "/login",
    element: <Login />,
  },
]);

export default router;